interface I {
    public abstract void play();
}

class B implements I {
    public void play() {
        System.out.println("play in a B class.");

    }
}

class C implements I {
    public void play() {
        System.out.println("play in a C class.");
    }
}

class A {
    void autoplay(I i) {
        i.play();
    }
}


class InterfaceTest2 {
    public static void main(String[] args) {
        // Enter Your Code Here
        A a = new A();
        a.autoplay(new B());
        a.autoplay(new C());
    }
}
